$(document).ready(function () {
	$('.owl-carousel').owlCarousel({
		items: 1,
		animateOut: 'fadeOut',
		animateIn: 'fadeIn',
		dots: false,
		URLhashListener: true,
	});
	$('.owl-carousel').on('mousewheel', '.owl-stage', function (e) {
		if (e.deltaY > 0) {
			$('.owl-carousel').trigger('next.owl.carousel');
		} else {
			$('.owl-carousel').trigger('prev.owl.carousel');
		}
		e.preventDefault();
	});
});
